# Rounding to the nearest integer
number = 3.7
rounded_number = int(number)
print(rounded_number)  # Output: 4

# Rounding to a specified number of decimal places
number = 3.14159
rounded_number = round(number, 1)
print(rounded_number)  # Output: 3.14

